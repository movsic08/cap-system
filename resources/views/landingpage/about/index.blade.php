                        <h2 class="text-2xl font-bold tracking-tight text-gray-50">Alaminos Cathedral History
                        </h2>
                        <p class="mt-6">
                            In 1743, the Zambal pioneers settled in what is now the seaside barangay Baleyadaan of
                            Alaminos, with the settlement named after their leader Suyang. There, they built houses
                            and a chapel dedicated to Saint Joseph. Due to the frequent typhoons, they resettled to the
                            wide plains southwest of their previous area and named it Casboran, in reference to the
                            plants abundant in their new settlement. A continued increase in population led into the
                            creation of a new local government with its church and convent being constructed in 1768. A
                            disagreement in town leadership led to the burning of the whole town including the church,
                            with its residents fleeing in all directions. Survivors of the fire resettled some 5
                            kilometres (3 miles) from the sea and was called Salapsap or Zarapzap.
                        </p>
                        <p class="mt-6">
                            The Augustinian Recollects, who are in charge of the area, then formally established a
                            parish, with Toribio Raymundo as the first parish priest who would go on to serve from 1778
                            until 1815. In 1834, the church was charred again. It was then reconstructed under the helm
                            of Manuel Busqueto from 1837 to 1849. The architect-engineer Maestro Cenon, who also
                            designed the church of Aguilar, was tapped by Busqueto to design the church of Sarapsap. The
                            entire town was told to contribute in rebuilding the church. Busqueto died after 16 years of
                            being Sarapsap's parish priest.
                        </p>
                        <p class="mt-6">
                            From 1849 to 1878, Jose Tornos continued the rebuilding of the new church. In 1857, the
                            church was inaugurated built with stone walls, nipa roofing, a tabernacle and a niche for
                            the church's patron, Saint Joseph. In 1860, Juan Alaminos y Vivar visited the town and in
                            1873, the town's name was changed into Alaminos in honor of the governor-general of the
                            Philippines.
                        </p>
                        <p class="mt-6">
                            Victoriano Vereciano became Alaminos' parish priest in 1879. In 14 years, he made
                            improvements in the Alaminos' church and convent which include: obtaining hardwood agaro
                            from the forests of Zaragoza and tindalo from Alos; galvanized iron sheets replaced the
                            church's nipa roof; installation of wood parquet for the loft's floor; the ceiling was
                            repainted; and communion rails made of iron were placed. A silver altar, a sanctuary light,
                            floors made of concrete, and church bells were also added or implemented.
                        </p>
                        <p class="mt-6">
                            Andres Romero succeeded Vereciano who died in 1893. Under his term, the cemetery was
                            enlarged and the chapel was reconstructed. During the Philippine-American War in 1901, the
                            Augustinian Recollects ceded the parish's administration to the local clergy.
                        </p>
                        <p class="mt-6">
                            Among the improvements made to the church of Alaminos from 1926 include: the Sacred Heart of
                            Jesus statue installation in 1930; construction of grotto of Our Lady of Lourdes with its
                            image sculpted by the late Filipino artist and clergyman, Lingayen-Dagupan Archbishop
                            Mariano Madriaga; adding of new church pews; the church belfry was remade into concrete;
                            fences were added for the church's front yard; new tabernacle installation; Our Lady of
                            Mount Carmel chapel and mortuary construction; and the installation of large Stations of the
                            Cross, done by a local artist.
                        </p>
                        <p class="mt-6">
                            The church of Alaminos was elevated into a cathedral when the Diocese of Alaminos was
                            founded in 1985. Bishop Jesus Cabrera, its first bishop, spearheaded more cathedral
                            improvements which include: installation of marble on the cathedral's altar and floor; new
                            sacristy; constructions of Alaminos Pastoral Center and perpetual adoration chapel; and the
                            repainting of the whole church in 1995.
                        </p>
